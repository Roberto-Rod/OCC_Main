/* I2C slave address */
#define STTS75_I2C_ID                   (0x90u)

/* Register mapping */
#define STTS75_TEMP                     (0x00u)
#define STTS75_CONF                     (0x01u)
#define STTS75_THYS                     (0x02u)
#define STTS75_TOS                      (0x03u)

