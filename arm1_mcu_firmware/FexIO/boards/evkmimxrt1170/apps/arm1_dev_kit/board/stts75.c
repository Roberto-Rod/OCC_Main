/**
 * @brief
 *   Read temperature from the STTS75 temperature sensor
 * @param
 *   p_value Address where 16-bit value is to be returned 
 * @return
 *   Status
 */
status_t STTS75_read_temperature(uint8_t *p_value)
{
    status_t status = I2C6_receive(
        STTS75_I2C_ID >> 1u, 
        STTS75_TEMP, 
        sizeof(uint8_t), 
        p_value, 
        sizeof(uint16_t));
  
    return status;
}

