/*******************************************************************************
 * Function:        MAV_Filter( uint16_t* data, uint16_t length, uint16_t width, uint16_t passes, bool dir )
 * 
 * Precondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Returns:         None
 *
 * Side Effects:    None
 *
 * Overview:        Applies a n point, m pass moving average filter  
 *
 * Notes:           None
 ******************************************************************************/
bool MAV_Filter( uint16_t* data, uint16_t length, uint16_t width, uint16_t passes, bool dir )
{
    bool saturated = false;
    uint16_t i, j;
    uint16_t fOffset;
    uint16_t bOffset;
    uint16_t fLength;
    uint32_t tempsum;
    
    if(( width > 0U )&&( length >= width ))
    {
    
        fOffset = width / 2U;
        bOffset = fOffset;
        fLength = length - fOffset;    

        if( dir == FWD )
        {
            for( j = passes; j > 0; j-- )
            {
                tempsum = 0U;

                for( i = 0; i < width; i++ )
                {
                    if( data[i] > SCAN_SATURATION_LIMIT )
                        saturated = true;

                    tempsum = tempsum + data[i];
                    data[i] = tempsum / ( i + 1 );
                }	

                for( i = ( bOffset + 1U ); i < fLength; i++ )
                {
                    if( data[i] > SCAN_SATURATION_LIMIT )
                        saturated = true;            

                    tempsum = tempsum + data[i + fOffset];
                    tempsum = tempsum - data[i - bOffset];
                    data[i] = tempsum / width;	
                }	
            }
        }
        else
        {
            for( j = passes; j > 0; j-- )
            {
                tempsum = 0U;

                for( i = 0; i < width; i++ )
                {
                    uint16_t idx  = length -1-i; 
                    if( data[idx] > SCAN_SATURATION_LIMIT )
                        saturated = true;

                    tempsum = tempsum + data[idx];
                    data[idx] = tempsum / ( i + 1 );
                }	

                for( i = (fLength-1); i >= ( bOffset + 1U ); i-- )
                {
                    if( data[i] > SCAN_SATURATION_LIMIT )
                        saturated = true;            

                    tempsum = tempsum + data[i - fOffset];
                    tempsum = tempsum - data[i + bOffset];
                    data[i] = tempsum / width;	
                }	
            }        
        }
    }
    
    return saturated;
}
